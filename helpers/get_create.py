def get_creation_date(user_id: int) -> str:
    """
    يُرجع تاريخ إنشاء الحساب التقريبي بالعربية بناءً على الـ user_id.
    """
    uid = abs(int(user_id))

    # إيجاد النقطة المرجعية الأقرب
    label = _CHECKPOINTS[0][1]
    for threshold, date_label in _CHECKPOINTS:
        if uid >= threshold:
            label = date_label
        else:
            break

    return f"~ {label}"
